#include "../../includes/push_swap.h"

int	pile_size(t_pile *p)
{
	t_pile	*tmp;
	int		i;

	tmp = p;
	i = 0;
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
