#include "../includes/push_swap.h"

void	sort_show(int *tab, int size)
{
	int		i;

	i = 0;
	while (i < size)
	{
		ft_putnbr_fd(tab[i], 1);
		write(1, "\n", 1);
		i++;
	}
}